CREATE TABLE IF NOT EXISTS CHEF (
    EMP_ID		     INT PRIMARY KEY NOT NULL,
    CHEF_NAME        CHAR(10) DEFAULT NULL,
    SALARY           FLOAT(7,2) DEFAULT NULL,
    DATAOFBRINTH     DATE DEFAULT NULL,
    BID_FK	         INT NOT NULL REFERENCES BRANCES(BID)
 );



--
-- Table structure for table `WAITERS`
--

CREATE TABLE IF NOT EXISTS WAITERS (
    EMP_ID		     INT PRIMARY KEY NOT NULL,
    WAITER_NAME      CHAR(10) DEFAULT NULL,
    SALARY           FLOAT(7,2) DEFAULT NULL,
    DATAOFBRINTH     DATE DEFAULT SYSDATE(),
    BID	             INT NOT NULL REFERENCES BRANCHES(BID)
 );




--
-- Table structure for table `DELIVERYBOY`
--

CREATE TABLE IF NOT EXISTS DELIVERYBOY (
    EMP_ID		     INT PRIMARY KEY NOT NULL,
    D_NAME           CHAR(10) DEFAULT NULL,
    SALARY           FLOAT(7,2) DEFAULT NULL,
    DATA_OF_BRITH    DATE DEFAULT SYSDATE(),
    BID	             INT NOT NULL REFERENCES BRANCES (BID)
);

