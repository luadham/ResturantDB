-- Food Item
CREATE TABLE IF NOT EXISTS FOOD_ITEMT (
    FID INT PRIMARY KEY NOT NULL,
    FOOD_NAME VARCHAR(20),
    PRICE FLOAT(4,1),
    ORDER_ID INT NOT NULL REFERENCES ORDERS(ORDER_ID)
