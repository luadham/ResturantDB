-- Branches Table
CREATE TABLE IF NOT EXISTS BRANCHES (
    BID INT PRIMARY KEY NOT NULL,
    BRANCH_NAME VARCHAR(10) DEFAULT NULL,
    BRANCH_ADDRESS VARCHAR(10) DEFAULT NULL
);

-- Orders Table
CREATE TABLE IF NOT EXISTS ORDERS (
    ORDER_ID INT PRIMARY KEY NOT NULL,
    ORDER_TIME TIME,
    ORDER_DATE DATE,
    TICKET_NO INT,
    BID INT NOT NULL REFERENCES BRANCES (BID),
    CID INT NOT NULL REFERENCES CUSTOMERS (CID),
    PAYMENT_ID INT NOT NULL REFERENCES PAYMENT (PAYMENT_ID)
);

-- Customers Table
CREATE TABLE IF NOT EXISTS CUSTOMERS (
    CID INT PRIMARY KEY NOT NULL,
    PHONE VARCHAR(20) DEFAULT NULL,
    CUSTOMER_NAME VARCHAR(20) NOT NULL,
    CUSTOMER_ADR VARCHAR(20)
);

